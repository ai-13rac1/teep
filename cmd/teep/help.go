// findFactorByName returns the factorInfo for the given name.
func findFactorByName(name string) (factorInfo, bool) {
	for _, f := range factorRegistry {
		if f.Name == name {
			return f, true
		}
	}
	return factorInfo{}, false
}

// runHelp dispatches to the appropriate help printer.
func runHelp(args []string) {
	if len(args) == 0 {
		printOverview()
		return
	}
	switch args[0] {
	case "serve":
		printServeHelp()
	case "verify":
		printVerifyHelp()
	case "tiers":
		printTiersHelp()
	case "factors":
		printFactorsHelp()
	case "measurements":
		printMeasurementsHelp()
	case "self-check":
		printSelfCheckHelp()
	default:
		if f, ok := findFactorByName(args[0]); ok {
			printFactorHelp(f)
			return
		}
		fmt.Fprintf(os.Stderr, "teep help: unknown topic %q\n\n", args[0])
		printOverview()
	}
}

