// wrapText wraps text to the given width with the given indent. Each line
// is prefixed with indent spaces. Words are not broken.
func wrapText(text string, indent, width int) string {
	words := strings.Fields(text)
	if len(words) == 0 {
		return ""
	}

	prefix := strings.Repeat(" ", indent)
	maxLineLen := width - indent

	var b strings.Builder
	lineLen := 0

	for i, word := range words {
		wLen := len(word)
		if i == 0 {
			b.WriteString(prefix)
			b.WriteString(word)
			lineLen = wLen
			continue
		}
		if lineLen+1+wLen > maxLineLen {
			b.WriteByte('\n')
			b.WriteString(prefix)
			b.WriteString(word)
			lineLen = wLen
		} else {
			b.WriteByte(' ')
			b.WriteString(word)
			lineLen += 1 + wLen
		}
	}

	return b.String()
}
